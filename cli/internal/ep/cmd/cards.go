func newCardsReplaceCmd() *cobra.Command {
	var (
		bagID       string
		question    string
		answer      string
		hint        string
		explanation string
		contextText string
	)

	cmd := &cobra.Command{
		Use:   "replace <card-id>",
		Short: "Replace flashcard content and reset its schedule",
		Long: `Replace a flashcard's study content, then reset its FSRS
schedule to the initial new-card state. This is intended for prompt
quality migrations where changing the question or hint should make
the card behave like a newly introduced card.

The command fetches the existing card first. If --answer,
--explanation, or --context is omitted, the current value is preserved.
The --question and --hint flags are required because they are the
fields usually being regenerated by the skill.`,
		Example: `  ep cards replace kx78x72vmrhxft4gdd38z7s4yd81bscy \
    --question "I felt ___ after reading the rejection letter." \
    --hint "discouraged, dejected, low-spirited"

  ep cards replace kx78x72vmrhxft4gdd38z7s4yd81bscy --bag k17abc... \
    --answer "disheartened" \
    --question "I felt ___ after reading the rejection letter." \
    --hint "discouraged, dejected, low-spirited" \
    --json`,
		Args: cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			cardID := strings.TrimSpace(args[0])
			if cardID == "" {
				return common.NewTokenError(
					common.TokenMissingRequiredField,
					"card-id is required",
					nil,
				)
			}
			if strings.TrimSpace(question) == "" {
				return common.NewTokenError(
					common.TokenMissingRequiredField,
					"--question is required",
					nil,
				)
			}
			if strings.TrimSpace(hint) == "" {
				return common.NewTokenError(
					common.TokenMissingRequiredField,
					"--hint is required",
					nil,
				)
			}
			if cmd.Flags().Changed("answer") && strings.TrimSpace(answer) == "" {
				return common.NewTokenError(
					common.TokenMissingRequiredField,
					"--answer must not be blank when provided",
					nil,
				)
			}

			resolvedBag, err := cardsResolveBagIDFunc(bagID)
			if err != nil {
				return err
			}

			if jsonFlag.Used && len(jsonFlag.Fields) == 0 {
				common.PrintFieldList(append(
					[]common.Field{{Name: "ok", Type: "boolean"}},
					cardsReplaceFields...,
				))
				return nil
			}

			ctx := cmd.Context()
			client, user, err := cardsAuthenticatedClientFunc(ctx)
			if err != nil {
				return err
			}

			existing, err := cardsGetCardFunc(ctx, client, user.ID, resolvedBag, cardID)
			if err != nil {
				return err
			}

			nextAnswer := strings.TrimSpace(answer)
			if nextAnswer == "" {
				nextAnswer = existing.Answer
			}

			nextExplanation := existing.Explanation
			if cmd.Flags().Changed("explanation") {
				value := strings.TrimSpace(explanation)
				nextExplanation = &value
			}

			nextContext := existing.Context
			if cmd.Flags().Changed("context") {
				value := strings.TrimSpace(contextText)
				nextContext = &value
			}

			replacement := cardReplacement{
				Question:    strings.TrimSpace(question),
				Answer:      nextAnswer,
				Hint:        strings.TrimSpace(hint),
				Explanation: nextExplanation,
				Context:     nextContext,
				SourceWord:  existing.SourceWord,
				Expression:  existing.Expression,
			}
			if err := cardsReplaceCardContentAndResetScheduleFunc(ctx, client, resolvedBag, cardID, replacement); err != nil {
				return err
			}

			card := cardDetail{
				ID:          existing.ID,
				Question:    replacement.Question,
				Answer:      replacement.Answer,
				Hint:        &replacement.Hint,
				Explanation: replacement.Explanation,
				Context:     replacement.Context,
				SourceWord:  replacement.SourceWord,
				Expression:  replacement.Expression,
			}
			payload := cardOutputPayload(card, resolvedBag)
			if handled, err := jsonFlag.HandleOKOutput(payload, cardsReplaceFields); handled {
				return err
			}

			fmt.Printf("Replaced card %s in bag %s and reset its schedule:\n", cardID, resolvedBag)
			fmt.Printf("  Q: %s\n", replacement.Question)
			fmt.Printf("  A: %s\n", replacement.Answer)
			fmt.Printf("  Hint: %s\n", replacement.Hint)
			return nil
		},
	}

	cmd.Flags().StringVar(&bagID, "bag", "", "Target bag ID. Falls back to default_bag_id in the config file.")
	cmd.Flags().StringVar(&question, "question", "", "Replacement fill-in-the-blank question with ___ for the answer. Required.")
	cmd.Flags().StringVar(&answer, "answer", "", "Replacement answer. Defaults to the current answer when omitted.")
	cmd.Flags().StringVar(&hint, "hint", "", "Replacement hint, usually 2-3 short synonyms or paraphrases. Required.")
	cmd.Flags().StringVar(&explanation, "explanation", "", "Replacement explanation. Defaults to the current explanation when omitted.")
	cmd.Flags().StringVar(&contextText, "context", "", "Replacement context. Defaults to the current context when omitted.")
	return cmd
}

func newCardsDeleteCmd() *cobra.Command {
	var bagID string

	cmd := &cobra.Command{
		Use:   "delete <card-id>",
		Short: "Soft-delete a flashcard",
		Long: `Soft-delete a flashcard by id. Calls the Convex
learning:deleteCard mutation, which marks the card with deletedAt
and decrements the bag's per-state counters.

The --bag flag is optional and falls back to default_bag_id in
the config file (set via 'ep bags default set'). The mutation
requires both cardId AND bagId, so a card from a non-default bag
must be deleted with --bag explicitly.

Idempotent: deleting a non-existent or already-deleted card is a
no-op and returns successfully. The skill should treat the
absence of the card as success on retry.`,
		Example: `  # Delete a card in the default bag
  ep cards delete kx78x72vmrhxft4gdd38z7s4yd81bscy

  # Explicit bag
  ep cards delete kx78x72vmrhxft4gdd38z7s4yd81bscy --bag k17abc...

  # JSON output (emits {"ok": true, "cardId": ..., "bagId": ...})
  ep cards delete kx78x72vmrhxft4gdd38z7s4yd81bscy --json`,
		Args: cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			cardID := strings.TrimSpace(args[0])
			if cardID == "" {
				return common.NewTokenError(
					common.TokenMissingRequiredField,
					"card-id is required",
					nil,
				)
			}

			resolvedBag, err := resolveBagID(bagID)
			if err != nil {
				return err
			}

			if jsonFlag.Used && len(jsonFlag.Fields) == 0 {
				common.PrintFieldList(append(
					[]common.Field{{Name: "ok", Type: "boolean"}},
					cardsDeleteFields...,
				))
				return nil
			}

			ctx := cmd.Context()

			client, _, err := authenticatedClient(ctx)
			if err != nil {
				return err
			}

			if _, err := client.Mutation(ctx, "learning:deleteCard", map[string]any{
				"cardId": cardID,
				"bagId":  resolvedBag,
			}); err != nil {
				return err
			}

			payload := map[string]any{
				"cardId": cardID,
				"bagId":  resolvedBag,
			}
			if handled, err := jsonFlag.HandleOKOutput(payload, cardsDeleteFields); handled {
				return err
			}

			fmt.Printf("Deleted card %s from bag %s\n", cardID, resolvedBag)
			return nil
		},
	}

	cmd.Flags().StringVar(&bagID, "bag", "", "Target bag ID. Falls back to default_bag_id in the config file.")
	return cmd
}
